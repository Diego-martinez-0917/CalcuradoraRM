package com.example.drica.calcuradorarm.Model;

public class DataRM {

    private int RM,peso,repe,metodo;
    private String comentario,nombreejer;

    public DataRM() {
    }

    public int getRM() {
        return RM;
    }

    public void setRM(int RM) {
        this.RM = RM;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

    public int getRepe() {
        return repe;
    }

    public void setRepe(int repe) {
        this.repe = repe;
    }

    public int getMetodo() {
        return metodo;
    }

    public void setMetodo(int metodo) {
        this.metodo = metodo;
    }

    public String getNombreejer() {
        return nombreejer;
    }

    public void setNombreejer(String nombreejer) {
        this.nombreejer = nombreejer;
    }
}
